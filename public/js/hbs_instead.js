$(function(){
    $.ajax({
        url:"header.html",
        type:"get",
        success:function(html){
            $(html).replaceAll("#bhs_header");
        }
    })
    $.ajax({
        url:"navbox.html",
        type:"get",
        success:function(html){
            $(html).replaceAll("#bhs_sub_navBox");
        }
    })
    $.ajax({
        url:"footer.html",
        type:"get",
        success:function(html){
            $(html).replaceAll("#bhs_footer");
        }
    })
})